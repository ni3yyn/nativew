export const normalizeLanguage = (language) => {
  if (!language || typeof language !== 'string') return DEFAULT_LANGUAGE;
  const baseLanguage = language.toLowerCase().split('-')[0];
  return translations[baseLanguage] ? baseLanguage : DEFAULT_LANGUAGE;
};

export const t = (key, language = DEFAULT_LANGUAGE) => {
  const lang = normalizeLanguage(language);
  return translations[lang]?.[key] ?? translations[DEFAULT_LANGUAGE]?.[key] ?? key;
};

export const interpolate = (template, params = {}) => {
  if (!template || typeof template !== 'string') return template;
  return Object.entries(params).reduce((text, [param, value]) => {
    return text.replaceAll(`{{${param}}}`, String(value));
  }, template);
};

export const getLocalizedValue = (value, language = DEFAULT_LANGUAGE) => {
  if (!value) return '';
  if (typeof value === 'string') return value;
  const lang = normalizeLanguage(language);
  return value[lang] ?? value[DEFAULT_LANGUAGE] ?? '';
};
